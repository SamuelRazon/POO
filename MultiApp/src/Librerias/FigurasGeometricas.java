package Librerias;

/**
 * Clase abstracta que permite calcular área y perimetro de figuras geométricas
 */
public abstract class FigurasGeometricas {

    /**
     * Calcular area con valores dados
     * @return Área de la figura
     */
    public abstract float getArea();

    /**
     * Calcular perimetro con valores dados
     * @return Perimetro de la figura
     */
    public abstract float getPerimetro();
}
